    void cargarJlabelFactura(){
        Object row[] = new Object[1];
        for(Factura a:Dao.MostrarUltimaFactura()){                          
            row[0]=a.getIdFactura();
        }
        jLabel_idFactura.setText(row[0].toString());        
    }
    
    void cargarJlabelCliente(){
        Object row[] = new Object[3];
        for(Cliente a:Dao.MostrarCliente()){                          
            row[0]=a.getIdCliente();
            row[1]=a.getNombreCliente();
            row[2]=a.getTelefono();                       
        }
        jLabelIdCliente.setText(row[0].toString());
        jLabelNombre.setText(row[1].toString());
        jLabelTel.setText(row[2].toString());
    }
     void cargarJCombo(){           
        ArrayList<Categoria>lista=Dao.listarCategoria();
       for(int x=0;x<lista.size();x++){
           Categoria obj=new Categoria(lista.get(x).getIdCategoria(),lista.get(x).getNomCategoria());           
           jComboCategoria.addItem(obj);
       }
       }
     
      void cargarDatosTabla(){
          jTable.getTableHeader().setFont(new Font("Segoe UI",Font.BOLD,12));
         jTable.getTableHeader().setOpaque(false);         
         jTable.getTableHeader().setBackground(new Color(32,136,203));
         jTable.getTableHeader().setForeground(new Color(00,00,00));
           String[] titulos = {"ID","Producto","Cantidad","Precio"};
           DefaultTableModel modelo= new DefaultTableModel(null, titulos);
           jTable.setModel(modelo);
           cargarJlabelCliente();
           String idCliente=jLabelIdCliente.getText().toString();
           Dao my=new Dao();
           Detalle vo= new Detalle();
           ArrayList<Detalle>lista=my.getDetalle(idCliente);
         for(int i=0;i<lista.size();i++){              
            Object fila[] = new Object[4];
            vo = lista.get(i);
            fila[0]=vo.getProducto_id();
            fila[1]=vo.getProducto_nombre();
            fila[2]=vo.getCantidad();
            fila[3]=vo.getPrecio();            
             modelo.addRow(fila);
        }
        jTable.setModel(modelo);          
    }
      
       void limpiarDatos(){
        txt_nombre.setText("");
        txt_Precio.setText("");        
        txt_id.setText("");
        jSpinner.setValue(0);
    }
     
