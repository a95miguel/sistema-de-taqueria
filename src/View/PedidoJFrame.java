    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        // TODO add your handling code here:
         if (txt_nombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Verefique sus datos", "ERROR", JOptionPane.ERROR_MESSAGE);           
        }
        else{
        try{
            int value = (Integer)jSpinner.getValue();           
            int cantidad=value;             
                int precio=Integer.parseInt(txt_Precio.getText());
            int totalProducto=precio*cantidad;                
            Detalle a= new Detalle();
            a.setNum_detalle(jLabelIdCliente.getText());
            a.setFactura_id(jLabel_idFactura.getText());
            a.setProducto_id(txt_id.getText());
            a.setCantidad(Integer.toString(cantidad));
            a.setPrecio(Integer.toString(totalProducto));
            Dao.registrarDatalle(a);
            cargarJlabelCliente();
           limpiarDatos();
           cargarDatosTabla();
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
        }
    }//GEN-LAST:event_btn_agregarActionPerformed

